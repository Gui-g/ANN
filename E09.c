#include <stdio.h>

int main() {
    float X[] = {-5, -4.5, -4, -3.5, -3, -2.5, -2, -1.5, -1, -0.5, 0, 0.5, 1, 1.5, 2};
    float Y[] = {-4.36, 1.06, 4.86, -4.01, 2.53, -4.29, -4.14, -4.68, -0.42, -2.52, 3.29, 4.82, -3.19, -0.22, 3.66};
    float A[15], Div;
    int i, j;

    for(i=0;i<15;i++) {
        Div = 1;
        for(j=0;j<15;j++){
            if(j != i)
                Div *= (X[i] - X[j]);
        }
        A[i] = Y[i]/Div;
    }

    for(i=0;i<15;i++) {
        printf("a[%d] = %.15f\n", i, A[i]);
    }

    return 0;
}